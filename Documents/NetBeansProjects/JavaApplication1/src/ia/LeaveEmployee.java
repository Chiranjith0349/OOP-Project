        // TODO add your handling code here:
        try{
        String sq1 ="insert into new_schema.leave(emply_ID,start_date,end_date,reason) values(?,?,?,?)";
        SimpleDateFormat Dformat = new SimpleDateFormat("yyyy-MM-dd");
            Connection conn = Dashboard.MySQLJDBCUtil.getConnection();
            PreparedStatement pst = conn.prepareStatement(sq1);
            pst.setString(1, jLabel4.getText());
            if (jDateChooser1.getDate().equals("") && jDateChooser1.getDate().equals("")){
                JOptionPane.showMessageDialog(null, "Start Date and End Date is Mandetary");
                return;
            }
            pst.setString(2, Dformat.format(jDateChooser1.getDate()));
            pst.setString(3, Dformat.format(jDateChooser1.getDate()));
            pst.setString(4, jTextField4.getText());
            pst.execute();
            JOptionPane.showMessageDialog(null, "Saved");

            this.dispose();
        }catch(Exception e){
            JOptionPane.showMessageDialog(null, e);
        }

    private void jTextField2CaretUpdate(javax.swing.event.CaretEvent evt) {//GEN-FIRST:event_jTextField2CaretUpdate
        // TODO add your handling code here:
            try{
            Connection conn = MySQLJDBCUtil.getConnection();
            String sq1 = "Select * from department where depart = '"+jTextField8.getText()+"'";
            String sq2 = "Select * from bonus where depart = '"+jTextField8.getText()+"'";
            Statement stm = conn.createStatement();
            ResultSet rs = stm.executeQuery(sq1);
            Statement stm2 = conn.createStatement();
            ResultSet rs2 = stm2.executeQuery(sq2);
            int netpay;
            if(rs.next()){
            int totalpay = Integer.parseInt(jTextField2.getText())*Integer.parseInt(jTextField3.getText())*rs.getInt("pay_per_hour");
            if (totalpay < rs.getInt("min_pay")){
                totalpay = rs.getInt("min_pay");
            }else if(totalpay > rs.getInt("max_pay")){
                totalpay = rs.getInt("max_pay");
            }
            if(rs2.next()){
            netpay = (totalpay)+(Integer.parseInt(jTextField4.getText())*rs2.getInt("pay_per_point"))+ 
                     (Integer.parseInt(jTextField9.getText())*rs.getInt("pay_per_hour"));
            jTextField5.setText(Integer.toString(netpay));
            }
            }
            }catch(Exception e){
            
            }
    }//GEN-LAST:event_jTextField2CaretUpdate


    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        try{
            String sq1 = "update salary set hrs_worked = '"+jTextField2.getText()+"', extra_hrs = '"+jTextField9.getText()+"', "
                    + "days_wrk = '"+jTextField3.getText()+"', points = '"+jTextField4.getText()+"', depart = '"+jTextField8.getText()+"', "
                    + "net_pay = '"+jTextField5.getText()+"' where emply_ID = '"+jTextField1.getText()+"'";
            String sq2= "Select * from bonus where depart = '"+jTextField8.getText()+"'";
            Connection conn = MySQLJDBCUtil.getConnection();
            Statement stm2 = conn.createStatement();
            ResultSet rs2 = stm2.executeQuery(sq2);
            if(jTextField1.getText().equals("")){
                JOptionPane.showMessageDialog(null, "Enter a valid employee id");
                return;
            }
            try{
                Integer.parseInt(jTextField2.getText());
                Integer.parseInt(jTextField3.getText());
                Integer.parseInt(jTextField4.getText());
                Integer.parseInt(jTextField9.getText());
            }catch(Exception e){
                JOptionPane.showMessageDialog(null, "Enter valid data");
                return;
            }
            while (rs2.next()){
            if (Integer.parseInt(jTextField4.getText()) > rs2.getInt("max_point")){
            JOptionPane.showMessageDialog(null, "Number of points exceeds maximum points of the department");
            return;
            }
            
            }
            PreparedStatement pst = conn.prepareStatement(sq1);
            pst.execute();
            JOptionPane.showMessageDialog(null, "Saved");
        }catch(Exception e){
            JOptionPane.showMessageDialog(null, e);
        }
    }//GEN-LAST:event_jButton3ActionPerformed

